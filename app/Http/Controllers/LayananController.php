    {
        $l = new Layanan;
        $l->nama_layanan = $request->input('nama_layanan');
        $l->konfigurasi_layanan = $request->input('konfigurasi_layanan');
        $l->harga = $request->input('harga_layanan');
        $l->save();
        return redirect('/admin/layanan');
    }

